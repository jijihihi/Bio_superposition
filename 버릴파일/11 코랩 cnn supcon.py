                "class SafeInstanceNormalize:\n",
                '    """(x - mu) / max(std, threshold) on CHW tensor."""\n',
                "    def __init__(self, threshold: float = 0.01):\n",
                "        self.threshold = float(threshold)\n",
                "\n",
                "    def __call__(self, tensor: torch.Tensor) -> torch.Tensor:\n",
                "        mean = torch.mean(tensor, dim=[1, 2], keepdim=True)\n",

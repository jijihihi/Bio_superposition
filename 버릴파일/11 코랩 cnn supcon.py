                "# ==============================================================================\n",
                "# 4. Model\n",
                "# ==============================================================================\n",
                "class Encoder(nn.Module):\n",
                "    def __init__(self):\n",
                "        super().__init__()\n",
                "        self.layer1 = nn.Sequential(weight_norm(nn.Conv2d(3, 64, 3, 2, 1)), nn.ReLU())\n",
                "        self.layer2 = nn.Sequential(weight_norm(nn.Conv2d(64, 128, 3, 1, 1)), nn.ReLU())\n",
                "        self.layer3 = nn.Sequential(weight_norm(nn.Conv2d(128, 256, 3, 1, 2, dilation=2)), nn.ReLU())\n",
                "        self.layer4 = nn.Sequential(weight_norm(nn.Conv2d(256, 512, 3, 1, 4, dilation=4)), nn.ReLU())\n",
                "        self.layer5 = nn.Sequential(weight_norm(nn.Conv2d(512, 1024, 3, 1, 2, dilation=2)), nn.ReLU())\n",
                "        self.gap = nn.AdaptiveAvgPool2d((1, 1))\n",
                "\n",
                "    def forward(self, x, return_map=False):\n",
                "        x = self.layer1(x)\n",
                "        x = self.layer2(x)\n",
                "        x = self.layer3(x)\n",
                "        x = self.layer4(x)\n",
                "        feat_map = self.layer5(x)\n",
                "        pooled = self.gap(feat_map).view(feat_map.size(0), -1)\n",
                "        return (pooled, feat_map) if return_map else pooled\n",
                "\n",
                "\n",
                "class SupConModel(nn.Module):\n",
                "    def __init__(self, embed_dim=512):\n",
                "        super().__init__()\n",
                "        self.encoder = Encoder()\n",
                "        self.projector = nn.Sequential(\n",
                "            weight_norm(nn.Linear(1024, 1024)), nn.ReLU(),\n",
                "            weight_norm(nn.Linear(1024, embed_dim))\n",
                "        )\n",
                "\n",
                "    def forward(self, x):\n",
                "        pooled = self.encoder(x, return_map=False)\n",
                "        return F.normalize(self.projector(pooled), dim=1)\n",
                "\n",

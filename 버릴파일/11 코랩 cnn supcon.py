                '        self.scaler = torch.amp.GradScaler("cuda", enabled=torch.cuda.is_available())\n',
                '        self.device = torch.device("cuda" if torch.cuda.is_available() else "cpu")\n',
                "\n",
                '        self.best_loss = float("inf")\n',
                "        self.patience_counter = 0\n",
                "\n",
                "    def train_epoch(self, epoch: int):\n",
                "        self.model.train()\n",
                "        total_loss = 0.0\n",
                "        steps = 0\n",
                "\n",
                '        pbar = tqdm(self.train_loader, desc=f"Train E{epoch}/{self.args.epochs}", leave=True)\n',
                "        for batch in pbar:\n",
                "            if batch is None:\n",
                "                continue\n",
                "            view1, view2, labels = batch\n",
                "            if labels.numel() < 2:\n",
                "                continue\n",
                "\n",
                "            view1 = view1.to(self.device, non_blocking=True)\n",
                "            view2 = view2.to(self.device, non_blocking=True)\n",
                "            labels = labels.to(self.device, non_blocking=True)\n",
                "\n",
                "            images = torch.cat([view1, view2], dim=0)\n",
                "            labels2 = torch.cat([labels, labels], dim=0)\n",
                "\n",
                "            self.optimizer.zero_grad(set_to_none=True)\n",
                "\n",
                "            with torch.amp.autocast(device_type=self.device.type, enabled=torch.cuda.is_available()):\n",
                "                embeddings = self.model(images)\n",
                "                loss = self.criterion(embeddings, labels2)\n",
                "\n",
                "            self.scaler.scale(loss).backward()\n",
                "            self.scaler.step(self.optimizer)\n",
                "            self.scaler.update()\n",
                "\n",
                "            cur = float(loss.item())\n",
                "            total_loss += cur\n",
                "            steps += 1\n",
                '            pbar.set_postfix({"loss": f"{cur:.4f}"})\n',
                "\n",
                "        return 0.0 if steps == 0 else total_loss / steps\n",
                "\n",
                "    def validate(self, epoch: int):\n",
                "        self.model.eval()\n",
                "        total_loss = 0.0\n",
                "        steps = 0\n",
                "\n",
                '        pbar = tqdm(self.val_loader, desc=f"Val   E{epoch}/{self.args.epochs}", leave=True)\n',
                "        with torch.no_grad():\n",
                "            for batch in pbar:\n",
                "                if batch is None:\n",
                "                    continue\n",
                "                view1, view2, labels = batch\n",
                "                if labels.numel() < 2:\n",
                "                    continue\n",
                "\n",
                "                view1 = view1.to(self.device, non_blocking=True)\n",
                "                view2 = view2.to(self.device, non_blocking=True)\n",
                "                labels = labels.to(self.device, non_blocking=True)\n",
                "\n",
                "                images = torch.cat([view1, view2], dim=0)\n",
                "                labels2 = torch.cat([labels, labels], dim=0)\n",
                "\n",
                "                with torch.amp.autocast(device_type=self.device.type, enabled=torch.cuda.is_available()):\n",
                "                    embeddings = self.model(images)\n",
                "                    loss = self.criterion(embeddings, labels2)\n",
                "\n",
                "                cur = float(loss.item())\n",
                "                total_loss += cur\n",
                "                steps += 1\n",
                '                pbar.set_postfix({"val_loss": f"{cur:.4f}"})\n',
                "\n",
                "        return 0.0 if steps == 0 else total_loss / steps\n",
                "\n",
                "    def run(self):\n",
                '        logger.info(f"Starting Training on {self.device}")\n',
                "        os.makedirs(self.args.save_dir, exist_ok=True)\n",
                '        save_path = os.path.join(self.args.save_dir, "best_model.pt")\n',
                "\n",
                "        for epoch in range(1, self.args.epochs + 1):\n",
                "            # epoch 시작을 명시적으로 출력 (tqdm에 안 묻히게)\n",
                '            tqdm.write(f"\\n===== Epoch {epoch}/{self.args.epochs} =====")\n',
                "\n",
                "            train_loss = self.train_epoch(epoch)\n",
                "            val_loss = self.validate(epoch)\n",
                "\n",
                '            tqdm.write(f"Epoch {epoch:03d} | Train: {train_loss:.4f} | Val: {val_loss:.4f}")\n',
                "\n",
                "            if val_loss < self.best_loss:\n",
                "                self.best_loss = val_loss\n",
                "                self.patience_counter = 0\n",
                "                torch.save(self.model.state_dict(), save_path)\n",
                '                tqdm.write(f"  -> Saved Best Model (best_val={val_loss:.6f})")\n',
                "            else:\n",
